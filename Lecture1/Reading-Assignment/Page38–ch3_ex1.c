#include <stdio.h>

// Prints integers and floats.
int main(void) {
    // Declaration of variables with data types integer and float.
    int i, j;
    float x, y;

    // Gives values to the declared variables.
    i = 10;
    j = 20;
    x = 43.2892f;
    y = 5527.0f;

    // Prints the characters in the strings and the values of the declared variables.
    printf("i = %d, j = %d, x = %f, y = %f\n", i, j, x, y);
}